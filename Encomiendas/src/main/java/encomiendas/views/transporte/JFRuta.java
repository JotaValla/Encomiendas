    private void limpiarCampos() {
        txtDescripcion.setText("");
        txtListaParadas.setText("");
        cmbTipoRuta.setSelectedIndex(-1);
    }

    private void cargarTabla(Ruta ruta) {

        DefaultTableModel modeloTabla = new DefaultTableModel() {
            //filas y columnas no sean editables
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };

        //nombres de las columnas
        String titulos[] = {"ID", "Descripción", "Paradas", "Es Interprovincial"};
        //seteamos las columnas
        modeloTabla.setColumnIdentifiers(titulos);
        // Crear un objeto con los valores de la ruta
        Object[] objeto = {ruta.getId(), ruta.getDescripcion(), ruta.getParadas(), ruta.isEsInterprovincial()};

        // Agregar la fila al modelo de la tabla
        modeloTabla.addRow(objeto);

        // Establecer el modelo de la tabla
        tbRuta.setModel(modeloTabla);
    }

    private void cargarRutasCompletas() {

        try {
            DefaultTableModel modeloTabla = new DefaultTableModel() {
                //filas y columnas no sean editables
                @Override
                public boolean isCellEditable(int row, int column) {
                    return false;
                }
            };

            //nombres de las columnas
            String titulos[] = {"ID", "Descripción", "Paradas", "Es Interprovincial"};
            //seteamos las columnas
            modeloTabla.setColumnIdentifiers(titulos);
            List<Ruta> listaRutas = rutaController.listarRutas();
            if (listaRutas != null) {
                for (Ruta ruta : listaRutas) {
                    Object[] objeto = {ruta.getId(), ruta.getDescripcion(), ruta.getParadas(), ruta.isEsInterprovincial()};
                    modeloTabla.addRow(objeto);
                }
            }

            tablaRutasCompletas.setModel(modeloTabla);
        } catch (SQLException ex) {
            Logger.getLogger(JFRuta.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

    private void cargarRuta(int idRuta) {
        try {
            Ruta ruta = rutaController.obtenerRuta(idRuta);
            txtEditDescripcion.setText(ruta.getDescripcion());
            txtEditListaParadas.setText(ruta.getParadas());
            if (ruta.isEsInterprovincial() == true) {
                cmbEditTipoRuta.setSelectedIndex(0);
            } else {
                cmbEditTipoRuta.setSelectedIndex(1);
            }
        } catch (SQLException ex) {
            Logger.getLogger(JFRuta.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

