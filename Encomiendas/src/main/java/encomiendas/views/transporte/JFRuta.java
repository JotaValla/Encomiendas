    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        // TODO add your handling code here:
        String descripcion = txtDescripcion.getText();
        String paradas = txtListaParadas.getText();
        String tipoRuta = (String) cmbTipoRuta.getSelectedItem();
        boolean bTipoRuta;
        if (tipoRuta.equals("Interprovincial")) {
            bTipoRuta = true;
        } else {
            bTipoRuta = false;
        }
        Ruta ruta = new Ruta();
        ruta.setDescripcion(descripcion);
        ruta.setParadas(paradas);
        ruta.setEsInterprovincial(bTipoRuta);
        try {
            rutaController.crearRuta(ruta);
            JOptionPane.showMessageDialog(null, "Ruta agregada correctamente", "Información", JOptionPane.INFORMATION_MESSAGE);
            limpiarCampos();
        } catch (SQLException ex) {
            Logger.getLogger(JFRuta.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnAgregarActionPerformed

